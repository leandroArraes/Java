package variaveis;

public class TesteOperadoresMatematicos1 {
	public static void main(String[] args) {
		int a = 1;
		a = a + 1 ;
		
		int b = 4;
		b = b - 1;
		
		int novac = 5;
		novac = novac * 2;
		
		int d = 30;
		d = d / 2;
		
		int e = 10;
		e = e % 2;
		
		a++;
		a--;
		
		System.out.println("a = " + a ) ;
		System.out.println("b = " + b );
		System.out.println("c = " + novac );
		System.out.println("d = " + d );
		System.out.println("e = " + e );
	}

}
