package variaveis;

public class TesteOperadoresIncrementoDecremento1 {
	public static void main(String[] args) {
		System.out.println("pre-fixado");
		int x = 5;
		System.out.println("x = "+ x );
		System.out.println("++x = " + ++x );
		System.out.println("x = "+ x );
		System.out.println("--X = "+ --x );
		System.out.println("X="+x);
		System.out.println("Pos-fixado");
		System.out.println("x++ ="+ x++);
		System.out.println("x =" + x );
		System.out.println("x-- =" + x--);
		System.out.println("x = "+ x);
		
		
	}

}
