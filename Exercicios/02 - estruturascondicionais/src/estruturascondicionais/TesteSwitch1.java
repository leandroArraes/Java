package estruturascondicionais;

public class TesteSwitch1 {
	public static void main(String[] args) {
		int numero = 1;
		switch (numero) {
		case 1:
			System.out.println("variavel numero igual a 1");
			break;
		case 2:
			System.out.println("variavel numero igual 2");
			break;
		default:
			System.out.println("variavel numero e diferente de 1 e 2");
		}
	}

}
